CREATE TABLE notification_templates (
    template_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    template_name VARCHAR(120) NOT NULL UNIQUE,
    notification_type VARCHAR(80) NOT NULL,
    channels VARCHAR(255) NOT NULL,
    subject VARCHAR(255) NOT NULL,
    message_template VARCHAR(4000) NOT NULL,
    created_at TIMESTAMP NOT NULL,
    updated_at TIMESTAMP NOT NULL
);

CREATE TABLE notification_triggers (
    trigger_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    trigger_event VARCHAR(80) NOT NULL,
    template_id BIGINT NOT NULL,
    offset_minutes INT NOT NULL,
    enabled BOOLEAN NOT NULL,
    created_at TIMESTAMP NOT NULL,
    updated_at TIMESTAMP NOT NULL,
    CONSTRAINT fk_notification_trigger_template FOREIGN KEY (template_id)
        REFERENCES notification_templates(template_id)
);
